package com.a.enum_ex;

import java.util.Scanner;

public class C_Enum_Switch {

	public static void main(String[] args) {
		System.out.println("PE shirt size");
		Scanner scanner=new Scanner(System.in);
		String choice= scanner.nextLine();
		
		ShirtSize shirtSize= ShirtSize.valueOf(choice);
		switch (shirtSize) {
		case M:
			System.out.println("size is M");
			break;

		default:
			System.out.println("some other size");
			break;
		}
	}
}
